package stringMethods;

public class Concat {

	public static void main(String[] args) 
	{

		/**
		 *concat(String str):
		 *
		 *Concatenates the specified string to the end of this string.
		 */		
		
		String fname = " D ";
		
		String lname = "Vishnupriya";
		
		System.out.println(fname.concat(lname));


	}

}
